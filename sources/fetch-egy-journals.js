urls = urls.filter(a => a.startsWith('http'))

function fetchWithTimeout(url, timeout = 3000) {  // timeout in milliseconds
  return Promise.race([
      fetch(url),
      new Promise((_, reject) =>
          setTimeout(() => reject(new Error('Timeout')), timeout)
      )
  ]);
}

for await(let url of urls){
  try {
    const response = await fetchWithTimeout('https://example.com', 5000)
    if (!response.ok) {
        throw new Error('Network response was not ok');
    }
    // Process the response
  } catch (error) {
    console.log('Fetch error:', error.message);
  }
}

let responses = await Promise.allSettled(requests)

